    public void add(Producto c){
        JLabel label = new JLabel("label"+index);
        panel.add(label);
        stock.add(label);
        try{
            label.setIcon(new javax.swing.ImageIcon(getClass().getResource(c.getUrlImagen())));
        }catch(Exception e){
            System.out.println(e+"img");
        }
        label.setText(c.getNombreProducto());
        index++;
        panel.updateUI();
    }
